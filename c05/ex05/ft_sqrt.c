#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
	{
		return (1);
	}
	while (i < (nb / i))
	{
		i++;
		if (i == (nb / i) && (nb % i) == 0)
		{
			return(i);
		}
	}
	return (0);
}
